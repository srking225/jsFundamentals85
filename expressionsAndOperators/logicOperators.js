// Addition Operator
r += 1; //says r = r + 1

//Subtration Operator
r -= 1; //says r = r - 1

//Multiplication Operator
r *= 1; // says r = r * 1

//Divison Operator
r /= 1; // says r = r / 1

//Remainder or Modulus operator
r %= 1 // says r = r % 1 finds the remainder

//Exponential Operator
r **= 3;    // says r = r to the power of 3