/** Comparison Operatore */
 
// Equal to
// JS assumed that we are trying to check if the string '3' is equal to the number 3
'3' == 3;  

//  Strict Equal to
// Compares the datatype and value of the two values to check if they are equal
// '===' values will need to be exactly the same

console.log(3 === 3);   // true
console.log('3' === 3);     //false

// Not equal to
console.log('3' != 3);  //false

// Strict Not Equal To
console.log('3' !== 3); //true

// Greater Than
console.log(3 > 2); //true
console.log(2 > 3); //false

// Less Than
console.log(2 < 3);     //true

// Greater Than or Equal to
console.log(3 >= 2);    //true

// Less Than or Equal to
console.log(2 <= 3);    //true
console.log(3 <= 2);    //false

// And Operator
2 && 3;

//Or Operator
2 || 3;

